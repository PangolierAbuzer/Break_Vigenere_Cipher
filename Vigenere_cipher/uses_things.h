static const vector<pair<char, int>> assembling()
{
	map<char, int> assembling_values; // מבתוךע הכ ץנאםוםט


	// הכ ךאזהמדמ סטלגמכא אכפאגטעא ןנמבודאולס ןמ גסוי סענמךו ט סקטעאול ךמכ-גמ
	for (int i = 0; i < alphabet.size(); i++)
	{
		int count = 0;
		for (int j = 0; j < msg.length(); j++)
		{
			if (alphabet[i] == msg[j])
				count++;
		}
		assembling_values.emplace(alphabet[i], count); // המבאגכול ךכ‏ק ט חםאקוםטו ךאזהמדמ סטלגמכא
	}


	// מחהאול ט טםטצטאכטחטנףול גוךעמנ ןאנ 
	// (ןמ סףעט עמע זו לאן, םמ ךמעמני למזםמ סמנעטנמגאע)
	vector<pair<char, int>> sorted_assembling_values
	(assembling_values.begin(), assembling_values.end());

	// כלבהא-פףםךצט סמנעטנמגךט ןמ חםאקוםטל (ךאך-עמ גנמהו נאבמעאוע)
	sort(sorted_assembling_values.begin(), sorted_assembling_values.end(),
		[](const auto& a, const auto& b)
		{ return a.second < b.second; });


	return sorted_assembling_values; // גמחגנאשאולן ןמכףקוםםי מבתוךע
}


